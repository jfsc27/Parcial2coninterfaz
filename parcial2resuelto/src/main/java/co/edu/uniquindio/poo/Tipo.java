package co.edu.uniquindio.poo;

/**
 * Enumeración que representa los tipos de transacciones posibles en una cuenta bancaria.
 * Puede ser RETIRO o DEPÓSITO.
 */
public enum Tipo {
    /** Tipo de transacción para un retiro de dinero de la cuenta. */
    RETIRO,

    /** Tipo de transacción para un depósito de dinero en la cuenta. */
    DEPOSITO;
}

